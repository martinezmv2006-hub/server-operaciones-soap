package com.nexos.logica;

public class Ejercicio {
	
	public double suma(double valor1,double valor2)throws Exception {
		return valor1+valor2;
	}
	
	public double resta(double valor1,double valor2)throws Exception {
		return valor1-valor2;
	}
	
	public double multiplicacion(double valor1,double valor2)throws Exception {
		return valor1*valor2;
	}
	
	public double divicion(double valor1,double valor2)throws Exception {
		return valor1/valor2;
	}

}
